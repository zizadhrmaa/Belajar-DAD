-- 7.1 Peminjaman dasar selama 15 bulan.
-- Pola popularitas sengaja bervariasi agar ranking dan persentil bermakna.
WITH months AS (
    SELECT
        gs AS month_idx,
        (
            date_trunc('month', current_date)
            - (14 - gs) * interval '1 month'
        )::date AS month_start
    FROM generate_series(0, 14) AS gs
), book_month_plan AS (
    SELECT
        b.buku_id,
        m.month_idx,
        m.month_start,
        CASE
            WHEN b.buku_id <= 60 THEN ((b.buku_id - 1) % 5) + 1
            ELSE 6 + ((b.buku_id - 61) % 3)
        END AS cabang_id,
        (
            1
            + ((b.buku_id + m.month_idx * 2) % 5)
            + CASE WHEN (b.buku_id % 10) IN (9, 0) THEN 4 ELSE 0 END
        )::integer AS loan_count
    FROM buku AS b
    CROSS JOIN months AS m
), expanded AS (
    SELECT
        p.*,
        occ
    FROM book_month_plan AS p
    CROSS JOIN LATERAL generate_series(1, p.loan_count) AS occ
    WHERE NOT (
        (p.cabang_id = 8 AND p.month_idx = 8)
        OR (p.cabang_id = 7 AND p.month_idx = 5)
    )
), member_choice AS (
    SELECT
        e.*,
        e.cabang_id + 8 * ((e.buku_id * 7 + e.month_idx * 11 + e.occ * 13) % 90) AS raw_anggota_id
    FROM expanded AS e
), prepared AS (
    SELECT
        mc.*,
        CASE
            WHEN mc.raw_anggota_id <= 10 THEN mc.raw_anggota_id + 80
            ELSE mc.raw_anggota_id
        END AS anggota_id,
        (
            mc.month_start
            + ((mc.buku_id + mc.occ * 3) % 24) * interval '1 day'
            + ((mc.occ * 17) % 10) * interval '1 hour'
        ) AS tanggal_pinjam
    FROM member_choice AS mc
)
INSERT INTO peminjaman (
    tanggal_pinjam, tanggal_jatuh_tempo, eksemplar_id,
    anggota_id, petugas_id, tanggal_kembali, status
)
SELECT
    p.tanggal_pinjam,
    p.tanggal_pinjam + interval '14 days',
    e.eksemplar_id,
    p.anggota_id,
    ((p.cabang_id - 1) * 2) + 1 + ((p.occ - 1) % 2),
    p.tanggal_pinjam + (7 + ((p.buku_id + p.occ + p.month_idx) % 13)) * interval '1 day',
    CASE
        WHEN (7 + ((p.buku_id + p.occ + p.month_idx) % 13)) > 14 THEN 'terlambat'
        ELSE 'dikembalikan'
    END
FROM prepared AS p
JOIN LATERAL (
    SELECT ek.eksemplar_id
    FROM eksemplar AS ek
    WHERE ek.buku_id = p.buku_id
      AND ek.cabang_id = p.cabang_id
    ORDER BY ek.eksemplar_id
    LIMIT 1
) AS e ON true;

-- 7.2 Peminjaman cohort untuk menguji retensi bulan ke-1, ke-2, dan ke-3.
WITH member_base AS (
    SELECT
        a.anggota_id,
        a.cabang_id,
        date_trunc('month', a.tanggal_daftar)::date AS cohort_month,
        ((a.anggota_id - 1) / 60) AS cohort_idx,
        ((a.anggota_id - 1) % 60) + 1 AS member_pos
    FROM anggota AS a
    WHERE a.anggota_id > 10
), retention_months AS (
    SELECT * FROM (VALUES (1), (2), (3)) AS v(month_no)
), retained AS (
    SELECT
        mb.*,
        rm.month_no,
        CASE
            WHEN mb.cabang_id <= 5 THEN
                mb.cabang_id + 5 * ((mb.anggota_id + rm.month_no * 3) % 12)
            WHEN mb.cabang_id = 6 THEN
                61 + 3 * ((mb.anggota_id + rm.month_no) % 7)
            WHEN mb.cabang_id = 7 THEN
                62 + 3 * ((mb.anggota_id + rm.month_no) % 7)
            ELSE
                63 + 3 * ((mb.anggota_id + rm.month_no) % 6)
        END AS buku_id
    FROM member_base AS mb
    CROSS JOIN retention_months AS rm
    WHERE
        CASE rm.month_no
            WHEN 1 THEN mb.member_pos <= 42 + (mb.cohort_idx % 4) * 3
            WHEN 2 THEN mb.member_pos <= 33 + (mb.cohort_idx % 3) * 4
            WHEN 3 THEN mb.member_pos <= 24 + (mb.cohort_idx % 5) * 3
        END
), prepared AS (
    SELECT
        r.*,
        (
            r.cohort_month
            + r.month_no * interval '1 month'
            + ((r.member_pos * 3 + r.month_no) % 20) * interval '1 day'
            + interval '10 hours'
        ) AS tanggal_pinjam
    FROM retained AS r
)
INSERT INTO peminjaman (
    tanggal_pinjam, tanggal_jatuh_tempo, eksemplar_id,
    anggota_id, petugas_id, tanggal_kembali, status
)
SELECT
    p.tanggal_pinjam,
    p.tanggal_pinjam + interval '14 days',
    e.eksemplar_id,
    p.anggota_id,
    ((p.cabang_id - 1) * 2) + 1,
    p.tanggal_pinjam + (8 + ((p.anggota_id + p.month_no) % 12)) * interval '1 day',
    CASE
        WHEN (8 + ((p.anggota_id + p.month_no) % 12)) > 14 THEN 'terlambat'
        ELSE 'dikembalikan'
    END
FROM prepared AS p
JOIN LATERAL (
    SELECT ek.eksemplar_id
    FROM eksemplar AS ek
    WHERE ek.buku_id = p.buku_id
      AND ek.cabang_id = p.cabang_id
    ORDER BY ek.eksemplar_id
    LIMIT 1
) AS e ON true;

-- 7.3 Sepuluh anggota dengan aktivitas sangat tinggi.
-- Mereka hanya meminjam buku 1-20 agar soal NOT EXISTS memiliki hasil jelas.
WITH extra_plan AS (
    SELECT
        member_id,
        n,
        1 + ((member_id * 3 + n) % 20) AS buku_id,
        (
            date_trunc('month', current_date)
            - ((n - 1) % 15) * interval '1 month'
            + ((member_id * 2 + n) % 24) * interval '1 day'
            + interval '14 hours'
        ) AS tanggal_pinjam
    FROM generate_series(1, 10) AS member_id
    CROSS JOIN generate_series(1, 60) AS n
), prepared AS (
    SELECT
        ep.*,
        ((ep.buku_id - 1) % 5) + 1 AS cabang_id
    FROM extra_plan AS ep
)
INSERT INTO peminjaman (
    tanggal_pinjam, tanggal_jatuh_tempo, eksemplar_id,
    anggota_id, petugas_id, tanggal_kembali, status
)
SELECT
    p.tanggal_pinjam,
    p.tanggal_pinjam + interval '14 days',
    e.eksemplar_id,
    p.member_id,
    ((p.cabang_id - 1) * 2) + 1,
    p.tanggal_pinjam + (10 + ((p.member_id + p.n) % 10)) * interval '1 day',
    CASE
        WHEN (10 + ((p.member_id + p.n) % 10)) > 14 THEN 'terlambat'
        ELSE 'dikembalikan'
    END
FROM prepared AS p
JOIN LATERAL (
    SELECT ek.eksemplar_id
    FROM eksemplar AS ek
    WHERE ek.buku_id = p.buku_id
      AND ek.cabang_id = p.cabang_id
    ORDER BY ek.eksemplar_id
    LIMIT 1
) AS e ON true;

-- 7.4 Buku 31 memiliki aktivitas denda tinggi, tetapi tidak pernah dipinjam
-- oleh sepuluh anggota teraktif. Ini membuat soal pendapatan denda bermakna.
WITH special_plan AS (
    SELECT
        n,
        1 + 8 * ((n * 7) % 90) AS raw_anggota_id,
        (
            date_trunc('month', current_date)
            - ((n - 1) % 12) * interval '1 month'
            + ((n * 5) % 24) * interval '1 day'
            + interval '11 hours'
        ) AS tanggal_pinjam
    FROM generate_series(1, 50) AS n
), prepared AS (
    SELECT
        n,
        CASE WHEN raw_anggota_id <= 10 THEN raw_anggota_id + 80 ELSE raw_anggota_id END AS anggota_id,
        tanggal_pinjam
    FROM special_plan
)
INSERT INTO peminjaman (
    tanggal_pinjam, tanggal_jatuh_tempo, eksemplar_id,
    anggota_id, petugas_id, tanggal_kembali, status
)
SELECT
    p.tanggal_pinjam,
    p.tanggal_pinjam + interval '14 days',
    e.eksemplar_id,
    p.anggota_id,
    1,
    p.tanggal_pinjam + interval '25 days',
    'terlambat'
FROM prepared AS p
JOIN LATERAL (
    SELECT ek.eksemplar_id
    FROM eksemplar AS ek
    WHERE ek.buku_id = 31
      AND ek.cabang_id = 1
    ORDER BY ek.eksemplar_id
    LIMIT 1
) AS e ON true;

-- 7.5 Buku 65 dibuat populer pada cabang 7 agar analisis buku yang hanya
-- tersedia di cabang non-top-5 memiliki kandidat yang jelas.
WITH special_plan AS (
    SELECT
        n,
        7 + 8 * ((n * 11) % 90) AS raw_anggota_id,
        (
            date_trunc('month', current_date)
            - ((n - 1) % 12) * interval '1 month'
            + ((n * 7) % 24) * interval '1 day'
            + interval '15 hours'
        ) AS tanggal_pinjam
    FROM generate_series(1, 120) AS n
), prepared AS (
    SELECT
        n,
        CASE WHEN raw_anggota_id <= 10 THEN raw_anggota_id + 80 ELSE raw_anggota_id END AS anggota_id,
        tanggal_pinjam
    FROM special_plan
)
INSERT INTO peminjaman (
    tanggal_pinjam, tanggal_jatuh_tempo, eksemplar_id,
    anggota_id, petugas_id, tanggal_kembali, status
)
SELECT
    p.tanggal_pinjam,
    p.tanggal_pinjam + interval '14 days',
    e.eksemplar_id,
    p.anggota_id,
    13,
    p.tanggal_pinjam + (8 + (p.n % 10)) * interval '1 day',
    CASE WHEN (8 + (p.n % 10)) > 14 THEN 'terlambat' ELSE 'dikembalikan' END
FROM prepared AS p
JOIN LATERAL (
    SELECT ek.eksemplar_id
    FROM eksemplar AS ek
    WHERE ek.buku_id = 65
      AND ek.cabang_id = 7
    ORDER BY ek.eksemplar_id
    LIMIT 1
) AS e ON true;

-- Memastikan terdapat bulan nol transaksi pada cabang 7 dan 8.
-- Hal ini diperlukan untuk menguji calendar spine pada soal pertumbuhan bulanan.
DELETE FROM peminjaman AS p
USING eksemplar AS e
WHERE p.eksemplar_id = e.eksemplar_id
  AND (
        (
            e.cabang_id = 8
            AND date_trunc('month', p.tanggal_pinjam)
                = date_trunc('month', current_date) - interval '6 months'
        )
        OR
        (
            e.cabang_id = 7
            AND date_trunc('month', p.tanggal_pinjam)
                = date_trunc('month', current_date) - interval '9 months'
        )
      );

-- ============================================================
-- 8. DATA PEMBAYARAN DENDA
-- ============================================================

INSERT INTO pembayaran_denda (
    peminjaman_id, anggota_id, petugas_id, jumlah,
    tanggal_bayar, metode, status
)
SELECT
    p.peminjaman_id,
    p.anggota_id,
    p.petugas_id,
    round(
        (
            ((p.tanggal_kembali::date - p.tanggal_jatuh_tempo::date) * 1.75)
            + CASE
                WHEN e.buku_id = 31 THEN 125.00
                WHEN e.buku_id <= 20 THEN 35.00
                ELSE 0.00
              END
        )::numeric,
        2
    ) AS jumlah,
    p.tanggal_kembali + interval '1 day',
    CASE (p.peminjaman_id % 4)
        WHEN 0 THEN 'tunai'
        WHEN 1 THEN 'kartu_debit'
        WHEN 2 THEN 'transfer_bank'
        ELSE 'dompet_digital'
    END,
    'berhasil'
FROM peminjaman AS p
JOIN eksemplar AS e
    ON e.eksemplar_id = p.eksemplar_id
WHERE p.tanggal_kembali IS NOT NULL
  AND p.tanggal_kembali::date > p.tanggal_jatuh_tempo::date;

