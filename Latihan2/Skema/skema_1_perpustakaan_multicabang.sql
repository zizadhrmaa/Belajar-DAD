-- Setiap buku memiliki dua eksemplar pada cabang utama.
-- Sebagian buku memiliki satu eksemplar tambahan pada cabang kedua.
WITH copy_rows AS (
    SELECT
        b.buku_id,
        CASE
            WHEN b.buku_id <= 60 THEN ((b.buku_id - 1) % 5) + 1
            ELSE 6 + ((b.buku_id - 61) % 3)
        END AS cabang_id,
        gs AS copy_no
    FROM buku AS b
    CROSS JOIN generate_series(1, 2) AS gs

    UNION ALL

    SELECT
        b.buku_id,
        CASE
            WHEN b.buku_id <= 60 THEN
                CASE
                    WHEN (((b.buku_id - 1) % 5) + 1) = 5 THEN 1
                    ELSE (((b.buku_id - 1) % 5) + 2)
                END
            ELSE
                CASE
                    WHEN (6 + ((b.buku_id - 61) % 3)) = 8 THEN 6
                    ELSE (7 + ((b.buku_id - 61) % 3))
                END
        END AS cabang_id,
        3 AS copy_no
    FROM buku AS b
    WHERE b.buku_id % 4 = 0
), numbered AS (
    SELECT
        row_number() OVER (ORDER BY buku_id, cabang_id, copy_no) AS eksemplar_id,
        buku_id,
        cabang_id,
        copy_no
    FROM copy_rows
)
