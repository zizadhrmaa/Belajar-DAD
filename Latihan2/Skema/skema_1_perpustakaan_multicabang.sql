-- ============================================================
-- SKEMA 1: SISTEM PERPUSTAKAAN MULTI-CABANG
-- DBMS      : PostgreSQL 14+
-- Tujuan    : Dataset OLAP untuk 7 soal analitik perpustakaan
-- Catatan   : Data waktu dibuat relatif terhadap CURRENT_DATE,
--             sehingga analisis 12 bulan terakhir tetap relevan.
-- ============================================================

DROP SCHEMA IF EXISTS perpustakaan CASCADE;
CREATE SCHEMA perpustakaan;
SET search_path TO perpustakaan, public;

-- ============================================================
-- 1. TABEL MASTER WILAYAH DAN ALAMAT
-- ============================================================

CREATE TABLE provinsi (
    provinsi_id      integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nama_provinsi    varchar(100) NOT NULL UNIQUE,
    last_update      timestamp NOT NULL DEFAULT current_timestamp
);

CREATE TABLE kota (
    kota_id          integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nama_kota        varchar(100) NOT NULL,
    provinsi_id      integer NOT NULL REFERENCES provinsi(provinsi_id),
    last_update      timestamp NOT NULL DEFAULT current_timestamp,
    UNIQUE (nama_kota, provinsi_id)
);

CREATE TABLE alamat (
    alamat_id        integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    alamat           varchar(200) NOT NULL,
    alamat2          varchar(200),
    kecamatan        varchar(100) NOT NULL,
    kota_id          integer NOT NULL REFERENCES kota(kota_id),
    kode_pos         varchar(10),
    telepon          varchar(30),
    last_update      timestamp NOT NULL DEFAULT current_timestamp
);

-- ============================================================
-- 2. TABEL CABANG, PETUGAS, ANGGOTA
-- ============================================================

CREATE TABLE cabang (
    cabang_id            integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nama_cabang          varchar(120) NOT NULL UNIQUE,
    kepala_petugas_id    integer,
    alamat_id            integer NOT NULL REFERENCES alamat(alamat_id),
    tanggal_dibuka       date NOT NULL,
    aktif                boolean NOT NULL DEFAULT true,
    last_update          timestamp NOT NULL DEFAULT current_timestamp
);

CREATE TABLE petugas (
    petugas_id       integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nama_depan       varchar(80) NOT NULL,
    nama_belakang    varchar(80),
    email            varchar(150) NOT NULL UNIQUE,
    alamat_id        integer NOT NULL REFERENCES alamat(alamat_id),
    cabang_id        integer NOT NULL REFERENCES cabang(cabang_id),
    username         varchar(80) NOT NULL UNIQUE,
    password_hash    varchar(255) NOT NULL,
    aktif            boolean NOT NULL DEFAULT true,
    last_update      timestamp NOT NULL DEFAULT current_timestamp
);

CREATE TABLE anggota (
    anggota_id       integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nomor_anggota    varchar(30) NOT NULL UNIQUE,
    cabang_id        integer NOT NULL REFERENCES cabang(cabang_id),
    nama_depan       varchar(80) NOT NULL,
    nama_belakang    varchar(80),
    email            varchar(150) NOT NULL UNIQUE,
    alamat_id        integer NOT NULL REFERENCES alamat(alamat_id),
    tanggal_daftar   date NOT NULL,
    aktif            boolean NOT NULL DEFAULT true,
    last_update      timestamp NOT NULL DEFAULT current_timestamp
);

-- ============================================================
-- 3. TABEL BUKU, KATEGORI, PENULIS, PENERBIT
-- ============================================================

CREATE TABLE kategori (
    kategori_id      integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nama              varchar(100) NOT NULL UNIQUE,
    last_update       timestamp NOT NULL DEFAULT current_timestamp
);

CREATE TABLE penerbit (
    penerbit_id      integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nama             varchar(150) NOT NULL UNIQUE,
    alamat_id        integer NOT NULL REFERENCES alamat(alamat_id),
    email            varchar(150),
    telepon          varchar(30),
    last_update      timestamp NOT NULL DEFAULT current_timestamp
);

CREATE TABLE penulis (
    penulis_id       integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nama_depan       varchar(80) NOT NULL,
    nama_belakang    varchar(80),
    negara_asal      varchar(80),
    last_update      timestamp NOT NULL DEFAULT current_timestamp
);

CREATE TABLE buku (
    buku_id          integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    judul            varchar(250) NOT NULL,
    isbn             varchar(20) NOT NULL UNIQUE,
    tahun_terbit     integer NOT NULL CHECK (tahun_terbit BETWEEN 1900 AND 2100),
    penerbit_id      integer NOT NULL REFERENCES penerbit(penerbit_id),
    jumlah_halaman   integer NOT NULL CHECK (jumlah_halaman > 0),
    bahasa           varchar(50) NOT NULL DEFAULT 'Indonesia',
    sinopsis         text,
    aktif            boolean NOT NULL DEFAULT true,
    last_update      timestamp NOT NULL DEFAULT current_timestamp
);

CREATE TABLE buku_kategori (
    buku_id          integer NOT NULL REFERENCES buku(buku_id) ON DELETE CASCADE,
    kategori_id      integer NOT NULL REFERENCES kategori(kategori_id),
    last_update      timestamp NOT NULL DEFAULT current_timestamp,
    PRIMARY KEY (buku_id, kategori_id)
);

CREATE TABLE buku_penulis (
    buku_id          integer NOT NULL REFERENCES buku(buku_id) ON DELETE CASCADE,
    penulis_id       integer NOT NULL REFERENCES penulis(penulis_id),
    urutan_penulis   smallint NOT NULL DEFAULT 1 CHECK (urutan_penulis > 0),
    last_update      timestamp NOT NULL DEFAULT current_timestamp,
    PRIMARY KEY (buku_id, penulis_id)
);

-- ============================================================
-- 4. TABEL EKSEMPLAR, PEMINJAMAN, PEMBAYARAN DENDA
-- ============================================================

CREATE TABLE eksemplar (
    eksemplar_id     integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    kode_eksemplar   varchar(40) NOT NULL UNIQUE,
    buku_id          integer NOT NULL REFERENCES buku(buku_id),
    cabang_id        integer NOT NULL REFERENCES cabang(cabang_id),
    kode_rak         varchar(30) NOT NULL,
    tanggal_perolehan date NOT NULL,
    status           varchar(20) NOT NULL DEFAULT 'tersedia'
                     CHECK (status IN ('tersedia', 'dipinjam', 'rusak', 'hilang', 'perawatan')),
    last_update      timestamp NOT NULL DEFAULT current_timestamp
);

CREATE TABLE peminjaman (
    peminjaman_id        bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tanggal_pinjam       timestamp NOT NULL,
    tanggal_jatuh_tempo  timestamp NOT NULL,
    eksemplar_id         integer NOT NULL REFERENCES eksemplar(eksemplar_id),
    anggota_id           integer NOT NULL REFERENCES anggota(anggota_id),
    petugas_id           integer NOT NULL REFERENCES petugas(petugas_id),
    tanggal_kembali      timestamp,
    status               varchar(20) NOT NULL
                         CHECK (status IN ('dipinjam', 'dikembalikan', 'terlambat', 'hilang')),
    last_update          timestamp NOT NULL DEFAULT current_timestamp,
    CHECK (tanggal_jatuh_tempo > tanggal_pinjam),
    CHECK (tanggal_kembali IS NULL OR tanggal_kembali >= tanggal_pinjam)
);

CREATE TABLE pembayaran_denda (
    pembayaran_id    bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    peminjaman_id    bigint NOT NULL REFERENCES peminjaman(peminjaman_id) ON DELETE CASCADE,
    anggota_id       integer NOT NULL REFERENCES anggota(anggota_id),
    petugas_id       integer NOT NULL REFERENCES petugas(petugas_id),
    jumlah           numeric(12,2) NOT NULL CHECK (jumlah > 0),
    tanggal_bayar    timestamp NOT NULL,
    metode           varchar(30) NOT NULL
                     CHECK (metode IN ('tunai', 'kartu_debit', 'transfer_bank', 'dompet_digital')),
    status           varchar(20) NOT NULL
                     CHECK (status IN ('berhasil', 'tertunda', 'dibatalkan')),
    last_update      timestamp NOT NULL DEFAULT current_timestamp,
    UNIQUE (peminjaman_id)
);

-- FK kepala cabang ditambahkan setelah tabel petugas tersedia.
ALTER TABLE cabang
    ADD CONSTRAINT fk_cabang_kepala_petugas
    FOREIGN KEY (kepala_petugas_id) REFERENCES petugas(petugas_id);

